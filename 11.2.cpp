#include <stdio.h>

int main()
{
	int h;
	printf("nhap chieu cao tam giac: ");
	scanf("%d", &h);
	for (int t = 0; t <= h; t = t + 1)
	{
		for(int  i = 1; i <= h - i; i = i + 1)
		{
			printf("a");
			printf("\n");
		}
		for(int j = 1; j <= 2 * h + 1; j = j + 1)
		{
			printf("a");
			printf("\n");	
		}
	}
}
